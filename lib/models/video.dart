class Video {
  final String image;
  final String title;
  final String accountName;
  final String profilePhoto;
  final String views;
  final String uploadedTime;
  final bool isShorts;

  Video({
    required this.image,
    required this.title,
    required this.accountName,
    required this.profilePhoto,
    required this.views,
    required this.uploadedTime,
    required this.isShorts,
  });
}
